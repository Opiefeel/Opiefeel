age = 30 # int
height = 1.78 # float
is_male = True # bool
biography = 'hello world' # string

# type() - функция, которая возвращает тип данных

print(type(age))
print(type(height))
print(type(is_male))
print(type(biography))

# преобразование типов

age = str(age)
height = float(height)
is_male = int(is_male)
biography =  str(biography)

print(age)
print(height)
print(is_male)
print(biography)
